<p align="justify">
Awak akan memiliki karya, yang juga dikenali sebagai laman web, yang Kami reka bentuk untuk
Awak dan mana-mana visual yang Kami sediakan dengannya. Kami akan mengubah produk kerja kami,
termasuk apa-apa fail yang diperlukan, dan Awak akan bertanggungjawab untuk penyimpanan mereka.
Kami tidak perlu menyimpan salinan. Awak menjamin bahawa Awak mempunyai hak yang sah untuk semua
unsur teks, gambar, dan apa-apa lagi yang Awak berikan kepada Kami dan Awak tidak akan
bertanggungjawab untuk sebarang tuntutan pihak ketiga.</p>

<p align="justify">
Kami akan memiliki apa-apa kerja, idea, penemuan, produk, atau maklumat lain yang boleh hak cipta
yang kami buat berhubung dengan Perkhidmatan Kami menyediakan. Kami menjamin bahawa Kami mempunyai
hak undang-undang untuk semua elemen yang berkaitan dengan Perkhidmatan Kami menyediakan dan tidak
akan memegang Awak bertanggungjawab untuk sebarang tuntutan pihak ketiga.</p>

</li>
<li class="list-group-item">6. <span class="font-weight-bold">Kerahsiaan.</span>

<p align="justify">
Rahsia Awak selamat dengan Kami. Ini termasuk maklumat proprietari Awak (perkara seperti
rahsia perdagangan, pengetahuan, atau maklumat rahsia lain yang tidak tersedia secara umum). Kami
berjanji Kami tidak akan menjual maklumat proprietari Awak kepada pihak ketiga, tidak kira
betapa mereka menawarkan Kami dengan harta berbilion atau wanita atau kuasa atau apa-apa keistimewaan.</p>

<p align="justify">
Apa pun Kami akan maklumkan kepada Awak jika ada pihak ketiga merayu meminta data rahsia Awak.</p>

<p align="justify">
Tetapi jika pihak Kerajaan Malaysia yang minta, Kami akan serahkan seperti yang tertakluk dalam
undang-undang negara Malaysia tercinta.</p>

</li>
<li class="list-group-item">7. <span class="font-weight-bold">Tugasan.</span>

<p align="justify">
Pihak-Pihak tidak boleh menetapkan tanggungjawab yang mereka miliki di bawah Perjanjian ini kepada
orang lain melainkan kedua-dua Pihak bersetuju dengan penugasan secara bertulis.</p>
</li>
<li class="list-group-item">8. <span class="font-weight-bold">Penamatan.</span>

<p align="justify">
Kami sangat tidak suka melihat Engkau pergi. Jika Awak memutuskan Kami bukan cawan kopi
(atau wain), Awak boleh mengakhiri Perjanjian ini dengan memberikan Kami notis bertulis 
sepuluh (10) hari dan membayar Kami untuk Perkhidmatan yang Kami telah selesai.</p>

<p align="justify">
Jika mana-mana Pihak gagal mematuhi tanggungjawab atau kewajipan mereka di bawah Perjanjian ini,
Pihak yang satu lagi boleh menamatkan Perjanjian ini dengan memberi sepuluh (10) hari notis bertulis.
Perjanjian ini akan ditamatkan secara automatik apabila kedua-dua Pihak telah melaksanakan semua
tanggungjawab mereka di bawah Perjanjian dan semua pembayaran telah dibuat.</p>

<p align="justify">
Perjanjian ini akan ditamatkan secara automatik apabila kedua-dua Pihak telah melaksanakan semua
tanggungjawab mereka di bawah Perjanjian dan semua pembayaran telah dibuat.</p>

</li>
<li class="list-group-item">9. <span class="font-weight-bold">Batasan Liabiliti.</span>

<p align="justify">
Liabiliti Awak kepada Kami hanya untuk kos yang perlu dibayar di bawah Perjanjian ini. 
Awak tidak akan bertanggungjawab kepada Kami, atau mana-mana pihak ketiga, untuk ganti rugi 
seperti kehilangan keuntungan, kehilangan simpanan, kerosakan sampingan, kerosakan berbangkit,
atau ganti rugi khas.</p>

</li>
